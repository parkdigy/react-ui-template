let _colorScheme: 'light' | 'dark' = 'light';
let _theme: Theme = Theme;

const theme = {
  /********************************************************************************************************************
   * Color Scheme
   * ******************************************************************************************************************/

  setColorScheme(colorScheme: 'light' | 'dark') {
    _colorScheme = colorScheme;
  },

  getColorScheme() {
    return _colorScheme;
  },

  /********************************************************************************************************************
   * Theme
   * ******************************************************************************************************************/

  setTheme(theme: Theme) {
    _theme = theme;
  },

  getTheme() {
    return _theme;
  },
};

export default theme;
